CREATE TABLE pulseLog (
        id INT(10) UNSIGNED PRIMARY KEY NOT NULL AUTO_INCREMENT,
        `timeStamp` TIMESTAMP,
	`currentTime` DATETIME DEFAULT '1000-01-01 00:00:00',
	`timeDiff` INT(10) NULL DEFAULT '0', 
	`unixTime` INT(10) NULL DEFAULT '0',
	currentR1 FLOAT NULL DEFAULT '0',
	currentS2 FLOAT NULL DEFAULT '0',
	currentT3 FLOAT NULL DEFAULT '0',
	currentAverageR1 FLOAT NULL DEFAULT '0',
	currentAverageS2 FLOAT NULL DEFAULT '0',
	currentAverageT3 FLOAT NULL DEFAULT '0',
	temp0 FLOAT NULL DEFAULT '0',
	temp1 FLOAT NULL DEFAULT '0',
	temp2 FLOAT NULL DEFAULT '0',
	temp3 FLOAT NULL DEFAULT '0',
	temp4 FLOAT NULL DEFAULT '0',
	temp5 FLOAT NULL DEFAULT '0',
	pulses INT NULL DEFAULT '0',
	event char(255) NULL DEFAULT '0',
) CHARACTER SET UTF8;
